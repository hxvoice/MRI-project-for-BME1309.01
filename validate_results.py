from __future__ import annotations

import argparse
from dataclasses import dataclass
from pathlib import Path
import sys

import matplotlib.pyplot as plt
import numpy as np

PROJECT_ROOT = Path(__file__).resolve().parent
sys.path.insert(0, str(PROJECT_ROOT))
sys.path.insert(0, str(PROJECT_ROOT / "src"))

import pipeline_config as config
from mri_project.quantification import execute_template_matching


@dataclass(frozen=True)
class Tissue:
    name: str
    t1: float
    t2: float


TISSUES = (
    Tissue("WM", config.PHANTOM_TISSUES["wm"]["t1"], config.PHANTOM_TISSUES["wm"]["t2"]),
    Tissue("GM", config.PHANTOM_TISSUES["gm"]["t1"], config.PHANTOM_TISSUES["gm"]["t2"]),
    Tissue("CSF", config.PHANTOM_TISSUES["csf"]["t1"], config.PHANTOM_TISSUES["csf"]["t2"]),
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Validate generated MRF results against phantom and dictionary values.")
    parser.add_argument(
        "--phantom",
        type=Path,
        default=Path("data/processed/brain_param_map_2d.npy"),
        help="Ground-truth phantom parameter map.",
    )
    parser.add_argument(
        "--dictionary",
        type=Path,
        default=Path("data/processed/mrf_dictionary_data.npz"),
        help="Dictionary package generated by scripts/build_dictionary.py.",
    )
    parser.add_argument(
        "--coeff-maps",
        type=Path,
        default=Path("data/output/reconstructed_coeff_maps.npy"),
        help="Reconstructed subspace coefficient maps.",
    )
    parser.add_argument(
        "--reference-png",
        type=Path,
        default=Path("data/processed/simulated_brain_phantom.png"),
        help="Pre-generated PNG used as the visual reference.",
    )
    parser.add_argument(
        "--generated-png",
        type=Path,
        default=Path("data/output/quantitative_maps.png"),
        help="PNG rendered from the current code output.",
    )
    parser.add_argument(
        "--comparison-png",
        type=Path,
        default=Path("data/output/validation_comparison.png"),
        help="Side-by-side comparison PNG saved by this validation script.",
    )
    parser.add_argument(
        "--no-show",
        action="store_true",
        help="Save figures and print metrics without opening popup windows.",
    )
    return parser.parse_args()


def project_path(path: Path) -> Path:
    if path.is_absolute():
        return path
    return PROJECT_ROOT / path


def nearest_grid_value(grid: np.ndarray, value: float) -> float:
    return float(grid[int(np.argmin(np.abs(grid - value)))])


def load_matching_results(
    dictionary_path: Path,
    coeff_maps_path: Path,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    dictionary_data = np.load(dictionary_path)
    coeff_maps = np.load(coeff_maps_path)
    t1_grid = dictionary_data["t1"]
    t2_grid = dictionary_data["t2"]
    t1_map, t2_map, pd_map = execute_template_matching(
        coeff_maps,
        dictionary_data["compressed_dict"],
        t1_grid,
        t2_grid,
    )
    return t1_map, t2_map, pd_map, t1_grid, t2_grid


def render_quantitative_maps(
    t1_map: np.ndarray,
    t2_map: np.ndarray,
    pd_map: np.ndarray,
    save_path: Path,
) -> None:
    save_path.parent.mkdir(parents=True, exist_ok=True)
    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    im1 = axes[0].imshow(t1_map, cmap="magma", vmin=0, vmax=2000)
    axes[0].set_title("T1 Map (ms)")
    fig.colorbar(im1, ax=axes[0], fraction=0.046, pad=0.04)

    im2 = axes[1].imshow(t2_map, cmap="viridis", vmin=0, vmax=150)
    axes[1].set_title("T2 Map (ms)")
    fig.colorbar(im2, ax=axes[1], fraction=0.046, pad=0.04)

    im3 = axes[2].imshow(pd_map, cmap="bone")
    axes[2].set_title("PD Map (a.u.)")
    fig.colorbar(im3, ax=axes[2], fraction=0.046, pad=0.04)

    fig.suptitle("Current Quantitative Maps", fontsize=16)
    fig.tight_layout()
    fig.savefig(save_path, dpi=300, bbox_inches="tight")
    plt.close(fig)


def print_shape_checks(phantom: np.ndarray, t1_map: np.ndarray, t2_map: np.ndarray, pd_map: np.ndarray) -> None:
    print("\n[Shape checks]")
    print(f"phantom: {phantom.shape}, finite={np.isfinite(phantom).all()}")
    print(f"T1 map:  {t1_map.shape}, finite={np.isfinite(t1_map).all()}")
    print(f"T2 map:  {t2_map.shape}, finite={np.isfinite(t2_map).all()}")
    print(f"PD map:  {pd_map.shape}, finite={np.isfinite(pd_map).all()}")


def print_dictionary_comparison(
    phantom: np.ndarray,
    t1_map: np.ndarray,
    t2_map: np.ndarray,
    pd_map: np.ndarray,
    t1_grid: np.ndarray,
    t2_grid: np.ndarray,
) -> None:
    print("\n[Dictionary-value comparison]")
    header = (
        "Tissue  Pixels  True T1  Dict T1  Mean T1  MAE T1  Exact T1  "
        "True T2  Dict T2  Mean T2  MAE T2  Exact T2  Mean PD"
    )
    print(header)
    print("-" * len(header))

    for tissue in TISSUES:
        mask = (phantom[..., 0] == tissue.t1) & (phantom[..., 1] == tissue.t2)
        pixels = int(mask.sum())
        if pixels == 0:
            continue

        dict_t1 = nearest_grid_value(t1_grid, tissue.t1)
        dict_t2 = nearest_grid_value(t2_grid, tissue.t2)
        mean_t1 = float(np.mean(t1_map[mask]))
        mean_t2 = float(np.mean(t2_map[mask]))
        mae_t1 = float(np.mean(np.abs(t1_map[mask] - dict_t1)))
        mae_t2 = float(np.mean(np.abs(t2_map[mask] - dict_t2)))
        exact_t1 = float(np.mean(t1_map[mask] == dict_t1))
        exact_t2 = float(np.mean(t2_map[mask] == dict_t2))
        mean_pd = float(np.mean(pd_map[mask]))

        print(
            f"{tissue.name:<6} {pixels:>6} "
            f"{tissue.t1:>8.0f} {dict_t1:>8.0f} {mean_t1:>8.1f} {mae_t1:>7.1f} {exact_t1:>8.3f} "
            f"{tissue.t2:>8.0f} {dict_t2:>8.0f} {mean_t2:>8.1f} {mae_t2:>7.1f} {exact_t2:>8.3f} "
            f"{mean_pd:>7.2f}"
        )

    foreground = phantom[..., 2] > 0
    print(f"\nForeground pixels: {int(foreground.sum())}")
    print(f"Background mean PD: {float(np.mean(pd_map[~foreground])):.3f}")


def show_png_comparison(reference_png: Path, generated_png: Path, comparison_png: Path, show: bool) -> None:
    if not reference_png.exists():
        print(f"\n[Visual check] Reference PNG not found, skipped: {reference_png}")
        return
    if not generated_png.exists():
        print(f"\n[Visual check] Generated PNG not found, skipped: {generated_png}")
        return

    reference_image = plt.imread(reference_png)
    generated_image = plt.imread(generated_png)

    comparison_png.parent.mkdir(parents=True, exist_ok=True)
    fig, axes = plt.subplots(1, 2, figsize=(14, 6))
    axes[0].imshow(reference_image)
    axes[0].set_title("Reference PNG")
    axes[0].axis("off")
    axes[1].imshow(generated_image)
    axes[1].set_title("Current Code Output PNG")
    axes[1].axis("off")
    fig.tight_layout()
    fig.savefig(comparison_png, dpi=200, bbox_inches="tight")
    print(f"\n[Visual check] Saved side-by-side comparison to: {comparison_png}")
    if show:
        plt.show()
    plt.close(fig)


def main() -> None:
    args = parse_args()
    phantom_path = project_path(args.phantom)
    dictionary_path = project_path(args.dictionary)
    coeff_maps_path = project_path(args.coeff_maps)
    reference_png = project_path(args.reference_png)
    generated_png = project_path(args.generated_png)
    comparison_png = project_path(args.comparison_png)

    phantom = np.load(phantom_path)
    t1_map, t2_map, pd_map, t1_grid, t2_grid = load_matching_results(dictionary_path, coeff_maps_path)

    print_shape_checks(phantom, t1_map, t2_map, pd_map)
    print_dictionary_comparison(phantom, t1_map, t2_map, pd_map, t1_grid, t2_grid)

    render_quantitative_maps(t1_map, t2_map, pd_map, generated_png)
    print(f"\n[Render] Saved current quantitative maps to: {generated_png}")
    show_png_comparison(reference_png, generated_png, comparison_png, show=not args.no_show)


if __name__ == "__main__":
    main()
