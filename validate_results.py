_PYPLOT = None


def configure_pyplot(show: bool) -> None:
    global _PYPLOT

    if _PYPLOT is not None:
        return
    if not show:
        matplotlib.use("Agg")
    import matplotlib.pyplot as pyplot

    _PYPLOT = pyplot


def get_pyplot():
    if _PYPLOT is None:
        configure_pyplot(show=False)
    return _PYPLOT


@dataclass(frozen=True)
class Tissue:
    name: str
    t1: float
    t2: float


TISSUES = (
    Tissue("WM", config.PHANTOM_TISSUES["wm"]["t1"], config.PHANTOM_TISSUES["wm"]["t2"]),
    Tissue("GM", config.PHANTOM_TISSUES["gm"]["t1"], config.PHANTOM_TISSUES["gm"]["t2"]),
    Tissue("CSF", config.PHANTOM_TISSUES["csf"]["t1"], config.PHANTOM_TISSUES["csf"]["t2"]),
)


