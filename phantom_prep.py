# 导入核心依赖
import numpy as np  # 数值计算基础库，用于数组运算和掩码生成
from utils import save_npy, plot_phantom  # 导入自定义工具函数：保存数组和绘制体模参数图

# -----------------------------------------------------------------------------
# 体模参数配置（与轨迹生成参数严格匹配）
# -----------------------------------------------------------------------------
# 目标体模尺寸：220x220像素
# 与轨迹生成参数严格对应：FOV=220mm，RES=1mm → 220mm / 1mm/像素 = 220像素
TARGET_SHAPE = (220, 220)

# -----------------------------------------------------------------------------
# 生成简化三层脑体模
# 模型设计：采用同心圆分层结构，模拟大脑的白质、灰质、脑脊液三层解剖结构
# 参数来源：所有T1/T2/PD值均为3T场强下人体脑组织的标准测量值，保证仿真的生理真实性
# 项目用途：作为MRF前向仿真的"金标准"，用于验证后续重建和参数拟合算法的准确性
# -----------------------------------------------------------------------------
# 1. 创建归一化坐标网格
# 生成[-1, 1]区间的二维坐标网格，用于后续创建圆形掩码
x, y = np.meshgrid(
    np.linspace(-1, 1, TARGET_SHAPE[0]),
    np.linspace(-1, 1, TARGET_SHAPE[1])
)

# 2. 创建大脑整体掩码
# 半径0.8的圆形区域，模拟大脑的大致轮廓
brain_mask = (x**2 + y**2) < 0.8**2

# 3. 初始化三个参数矩阵
# 分别存储T1弛豫时间、T2弛豫时间、质子密度(PD)的空间分布
t1_2d = np.zeros(TARGET_SHAPE)  # 单位：毫秒(ms)
t2_2d = np.zeros(TARGET_SHAPE)  # 单位：毫秒(ms)
pd_2d = np.zeros(TARGET_SHAPE)  # 相对值，后续归一化到[0,1]

# 4. 白质(White Matter, WM)区域
# 半径0.5的中心圆形区域，模拟大脑深部白质
wm_mask = (x**2 + y**2) < 0.5**2
t1_2d[wm_mask] = 850   # 3T下白质T1标准值：800-900ms
t2_2d[wm_mask] = 60    # 3T下白质T2标准值：50-70ms
pd_2d[wm_mask] = 0.8   # 白质质子密度约为脑脊液的80%

# 5. 灰质(Gray Matter, GM)区域
# 大脑整体区域减去白质区域，模拟大脑皮层灰质
gm_mask = brain_mask & ~wm_mask
t1_2d[gm_mask] = 1350  # 3T下灰质T1标准值：1300-1400ms
t2_2d[gm_mask] = 80    # 3T下灰质T2标准值：70-90ms
pd_2d[gm_mask] = 0.9   # 灰质质子密度约为脑脊液的90%

# 6. 脑脊液(Cerebrospinal Fluid, CSF)区域
# 半径0.85的圆形区域减去大脑整体区域，模拟脑室和蛛网膜下腔的脑脊液
# ⚠️ 修正原代码的运算符优先级错误：&优先级高于<，必须加括号
csf_mask = ((x**2 + y**2) < 0.85**2) & ~brain_mask
t1_2d[csf_mask] = 4000  # 3T下脑脊液T1标准值：3500-4500ms
t2_2d[csf_mask] = 2000  # 3T下脑脊液T2标准值：1800-2200ms
pd_2d[csf_mask] = 1.0   # 脑脊液质子密度最高，作为归一化基准

# 7. 质子密度归一化
# 将PD值归一化到[0,1]区间，消除绝对值的影响，只保留相对对比度
pd_2d = pd_2d / np.max(pd_2d)

# -----------------------------------------------------------------------------
# 结果可视化与保存
# -----------------------------------------------------------------------------
# 绘制T1、T2、PD三张参数图并保存为高清图片
# 用于验证体模的分层结构和参数分布是否符合预期
plot_phantom(t1_2d, t2_2d, pd_2d, 
             save_path="../../data/processed/simulated_brain_phantom.png")

# 保存三维参数矩阵
# 形状：(220, 220, 3)，三个通道依次为[T1, T2, PD]
# 这是前向仿真的核心输入文件，后续代码会根据每个像素的参数生成对应的MR信号
param_map = np.stack([t1_2d, t2_2d, pd_2d], axis=-1)
save_npy(param_map, "../../data/processed/brain_param_map_2d.npy")

# 打印完成提示
print("✅ 模拟脑体模生成完成，已保存到 data/processed/")