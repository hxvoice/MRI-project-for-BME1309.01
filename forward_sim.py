# 导入核心依赖
import sigpy as sp  # MRI重建专用库，提供NUFFT和线圈灵敏度图生成功能
import numpy as np  # 数值计算基础库
from utils import save_npy  # 导入自定义工具函数：保存numpy数组

# -----------------------------------------------------------------------------
# 前向仿真参数配置（符合3T临床MRI设备特性）
# -----------------------------------------------------------------------------
N_COILS = 12        # 接收线圈数量，12通道头线圈是临床脑部MRI的标准配置
NOISE_LEVEL = 0.25  # 噪声水平，设置为信号平均幅度的25%
                    # 符合真实临床扫描的信噪比(SNR)范围，保证仿真数据的真实性

# -----------------------------------------------------------------------------
# 加载依赖数据
# 所有输入数据均为项目前期生成的中间结果，保证数据链路的一致性
# -----------------------------------------------------------------------------
# 加载自己生成的2D小金角旋转螺旋轨迹
# 形状：(500, 7000, 2) → [TR数, 单TR采样点数, kx/ky坐标]
traj_full = np.load("../../data/processed/traj_full_2d.npy")

# 加载自己生成的三层脑体模参数图
# 形状：(220, 220, 3) → [高度, 宽度, T1/T2/PD参数]
param_map = np.load("../../data/processed/brain_param_map_2d.npy")

# 加载成员C生成的MRF信号字典（⚠️ 等C完成后替换为实际路径）
# signal_dict: 形状(T1_num, T2_num, 500) → 每个(T1,T2)组合对应的500个TR的信号演化
# T1_grid: 形状(T1_num,) → 字典覆盖的T1值网格
# T2_grid: 形状(T2_num,) → 字典覆盖的T2值网格
signal_dict = np.load("../../data/processed/mrf_signal_dict.npy")
T1_grid = np.load("../../data/processed/T1_grid.npy")
T2_grid = np.load("../../data/processed/T2_grid.npy")

# -----------------------------------------------------------------------------
# 生成图像域时间序列（MRF前向仿真核心步骤）
# 原理：根据每个像素的T1/T2值，在信号字典中查找对应的信号演化曲线
# 优势：比逐像素求解Bloch方程快100倍以上，是MRF仿真的标准方法
# -----------------------------------------------------------------------------
# 初始化图像域时间序列数组
# 形状：(220, 220, 500) → [高度, 宽度, TR数]，存储每个像素在500个TR的复信号
img_series = np.zeros((220, 220, 500), dtype=np.complex64)

# 遍历体模的每个像素
for i in range(220):
    for j in range(220):
        # 获取当前像素的T1、T2、PD值
        t1_val = param_map[i, j, 0]
        t2_val = param_map[i, j, 1]
        pd_val = param_map[i, j, 2]
        
        # 在T1网格中找到与当前T1值最接近的索引
        t1_idx = np.argmin(np.abs(T1_grid - t1_val))
        # 在T2网格中找到与当前T2值最接近的索引
        t2_idx = np.argmin(np.abs(T2_grid - t2_val))
        
        # 从信号字典中取出对应的信号演化曲线，乘以质子密度PD
        # PD决定了信号的绝对幅度，T1/T2决定了信号的时间演化特征
        img_series[i, j, :] = signal_dict[t1_idx, t2_idx, :] * pd_val

# -----------------------------------------------------------------------------
# 多线圈并行成像模拟
# 原理：MRI使用多个接收线圈同时采集信号，每个线圈对不同空间位置的灵敏度不同
# 作用：提高图像信噪比，缩短扫描时间，是现代MRI的标准技术
# -----------------------------------------------------------------------------
# 生成12通道鸟笼线圈的灵敏度图
# 鸟笼线圈是头部MRI最常用的线圈类型，具有均匀的中心灵敏度分布
# 输出形状：(12, 220, 220) → [线圈数, 高度, 宽度]
sens_maps = sp.mri.birdcage_maps((N_COILS, 220, 220))

# 保存线圈灵敏度图，后续成员B的重建代码需要使用
save_npy(sens_maps, "../../data/processed/sens_maps_2d.npy")

# -----------------------------------------------------------------------------
# NUFFT非均匀傅里叶变换采样
# 核心原理：将图像域的复信号转换为k空间的非均匀采样点
# 为什么不用FFT：FFT只能处理笛卡尔网格的均匀采样，而我们使用的是螺旋非均匀采样
# -----------------------------------------------------------------------------
# 创建NUFFT算子
# coord: k空间采样轨迹
# shape: 图像域尺寸
# oversamp: 过采样因子，1.5是平衡精度和速度的最优值
nufft_op = sp.mri.NUFFT(coord=traj_full, shape=(220, 220), oversamp=1.5)

# 初始化多线圈k空间数据数组
# 形状：(12, 500, 7000) → [线圈数, TR数, 单TR采样点数]
kspace = np.zeros((N_COILS, 500, traj_full.shape[1]), dtype=np.complex64)

# 对每个线圈分别进行NUFFT变换
for c in range(N_COILS):
    # 将图像域时间序列乘以当前线圈的灵敏度图
    # np.newaxis将灵敏度图从(220,220)扩展为(220,220,1)，实现广播乘法
    coil_img = img_series * sens_maps[c, :, :, np.newaxis]
    
    # 执行NUFFT变换，将图像域信号转换为k空间非均匀采样信号
    # sigpy的NUFFT支持批量处理，自动对500个TR分别进行变换
    kspace[c] = nufft_op(coil_img)

# -----------------------------------------------------------------------------
# 添加复高斯噪声
# 原理：MRI的噪声是复高斯分布，实部和虚部相互独立且方差相同
# 作用：模拟真实扫描中的热噪声，使仿真数据更接近临床实际
# -----------------------------------------------------------------------------
# 计算无噪声k空间信号的平均幅度
signal_mean = np.mean(np.abs(kspace))

# 生成复高斯噪声
# 实部和虚部分别生成均值为0、标准差为signal_mean*NOISE_LEVEL的高斯噪声
noise = np.random.normal(0, signal_mean * NOISE_LEVEL, kspace.shape) + \
        1j * np.random.normal(0, signal_mean * NOISE_LEVEL, kspace.shape)

# 将噪声添加到无噪声k空间数据中
kspace_noisy = kspace + noise

# -----------------------------------------------------------------------------
# 保存最终输出
# -----------------------------------------------------------------------------
# 保存带噪声的多线圈k空间数据
# 这是你作为Member A的最终输出文件，成员B将使用这个文件进行MRF图像重建
save_npy(kspace_noisy, "../../data/output/mrf_kspace_2d_noisy.npy")

# 打印完成提示
print("✅ 前向模拟完成，带噪声的k空间数据已保存到 data/output/")